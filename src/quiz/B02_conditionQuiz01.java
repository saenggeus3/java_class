package quiz;

public class B02_conditionQuiz01 {
	
	public static void main(String[] args) {
		
		
		
		
		//1. int형 변수 a가 10보다 크고 20보다 작을 때 true
		int a=15;
		System.out.println(a>10 && a<20);
		//2. int형 변수 b가 짝수일 때 true
		int b = 10;
		System.out.println(b%2==0);
		//3. int형 변수 c가 7의 배수일 때 true
		int c = 49;
		System.out.println(c%7==0);
		//4. int형 변수 hour가 0미만 24이상이아니고 12이상일 때 true	
		int hour = 18;
		System.out.println(!(0>hour) && !(hour>=24) && hour>=12);
		//5. int형 변수 d와 e의 차이가 30일 때 true
		int d = 50, e = 20;
		System.out.println(d-e==30 || e-d==-30);
		//6. int형 변수 year가 400으로 나누어 떨어지거나 
		//   또는 4로 나누어 떨어지고 100으로 나누어떨어지지 않을 때 true
		int year = 2020;
		System.out.println(year%400==0 || (year%4==0 && !(year%100==0)));
		//7. 민수가 철수보다 2살 많으면 true
		int m_age1 = 24, c_age2 = 22;
		System.out.println(m_age1-c_age2>=2);
		int m_bir = 02, c_bir = 5;
		//8. 민수가 철수보다 생일이 3달 빠르면 true 
		System.out.println(c_bir-m_bir==3);
 		//9. boolean형 변수 powerOn이 false일 때 true
		boolean powerOn = false;
		System.out.println(!powerOn);
		
		//소문자로 시작하는 타입 - 값을 저장함
		//대문자로 시작하는 타입 - 주소값을 저장함(참조형)
		//참조형끼리 값을 비교하면 주소값을 비교함
		//문자열의 값을 비교하고싶을땐 .equals() 를 사용
		
		//10. 문자열 참조변수 str이 "yes"일 때 true
		String str = "yes";
		System.out.println(str.equals("yes"));
		
		
		//11. 삼각형A가 직각삼각형이면 true;
		int A_1 = 5 , A_2 = 3, A_3 = 4;
		System.out.println(
				Math.pow(A_1,2)+Math.pow(A_2,2)==Math.pow(A_3,2) ||
				Math.pow(A_1,2)+Math.pow(A_3,2)==Math.pow(A_2,2) || 
				Math.pow(A_3,2)+Math.pow(A_2,2)==Math.pow(A_1,2)
				);
		
		
	}

}
