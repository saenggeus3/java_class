
public class A02_variable {
	
	// 변수
	// 데이터를 미리 받아놓고 나중에 꺼내서 사용할 수 있는 공간
	// 변수를 사용하기 위해서는 해당 변수가 어떤 타입의 데이터를 저장할지 미리 정해야함 (선언, declare)
	
	public static void main(String[] args) {
		
		int a = 10;
		
		System.out.println(a);
		
		int num1;
		int num2,num3,num4;
		int num5 = 99;
		int num6 = 7, num7=31, num8=900;
		
		//값을 한번도 대입하지 않은 변수는 사용할 수 없음
		//변수를 선언한 뒤 맨 처음 값을 대입하는 것을 초기화(initialize) 라고 함
		num1 = 123; 
		System.out.println(num1);
		
		
	}
	
	

}
